package co.edu.unipiloto.simulacion.tarea.colas;

public class Client {
	private int arrivalTime, initServiceTime, serviceTime, enquedTime, departureTime;

	public int getArrivalTime() {
		return arrivalTime;
	}

	public void setArrivalTime(int arrivalTime) {
		this.arrivalTime = arrivalTime;
	}

	public int getInitServiceTime() {
		return initServiceTime;
	}

	public void setInitServiceTime(int initServiceTime) {
		this.initServiceTime = initServiceTime;
	}

	public int getServiceTime() {
		return serviceTime;
	}

	public void setServiceTime(int serviceTime) {
		this.serviceTime = serviceTime;
	}

	public int getEnquedTime() {
		return enquedTime;
	}

	public void setEnquedTime(int enquedTime) {
		this.enquedTime = enquedTime;
	}

	public int getDepartureTime() {
		return departureTime;
	}

	public void setDepartureTime(int departureTime) {
		this.departureTime = departureTime;
	}
	
	
}
